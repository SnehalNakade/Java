package AssignmentDay1;
/**
 * @author sneha-default values of primitive datatype
 *
 */
public class Defaultvalue {
			     int A;
			     char B;
				 float C;
				 double D;
				 long E;
				 short F;
				 byte G;
			     boolean H;
			    public static void main (String [] args) {
			    	Defaultvalue obj = new Defaultvalue() ;
			    	System.out.println("Default values : ");
			    	System.out.println("int : " + obj.A);
			    	System.out.println("char : " + obj.B);            
			    	System.out.println("float : " + obj.C);
			    	System.out.println("Double : " + obj.D);
			    	System.out.println("long : " + obj.E);
			    	System.out.println("short : " + obj.F);
			    	System.out.println("byte : " + obj.G);
			    	System.out.println("boolean : " + obj.H);	    	
			    }
}
